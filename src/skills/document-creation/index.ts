/**
 * Document Creation Skill Implementation
 * Creates documents in various formats with customizable templates
 * Uses pdfmake for PDF generation with table support
 */

import PdfPrinter = require('pdfmake');
import { writeFileSync, mkdirSync, existsSync } from 'fs';
import { dirname } from 'path';

// Define fonts for pdfmake
const fonts = {
  Helvetica: {
    normal: 'Helvetica',
    bold: 'Helvetica-Bold',
    italics: 'Helvetica-Oblique',
    bolditalics: 'Helvetica-BoldOblique'
  }
};

interface TableData {
  headers: string[];
  rows: string[][];
  widths?: Array<number | string>;
}

interface DocumentParams {
  format: 'pdf' | 'docx';
  template: 'report' | 'article' | 'analysis' | 'letter' | 'custom';
  language: 'en' | 'pl';
  content: {
    title: string;
    author?: string;
    date?: string;
    sections: Array<{
      heading: string;
      content: string;
      subsections?: Array<{
        heading: string;
        content: string;
      }>;
      tables?: Array<TableData>;
    }>;
  };
  output?: {
    filename?: string;
    directory?: string;
  };
}

interface DocumentResult {
  success: boolean;
  filePath: string;
  fileName: string;
  fileSize: number;
  format: string;
  createdAt: string;
}

