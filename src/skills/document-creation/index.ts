/**
 * Main function to create documents
 */
export async function documentCreation(params: DocumentParams): Promise<DocumentResult> {
  const {
    format,
    template,
    language,
    content,
    output = {}
  } = params;

  const resultsDir = output.directory || '/app/results';
  const timestamp = new Date().toISOString().replace(/[:.]/g, '-');
  const fileName = output.filename || `${content.title.replace(/[^a-zA-Z0-9]/g, '_')}_${timestamp}`;
  const fileExt = format === 'pdf' ? 'pdf' : 'docx';
  const filePath = `${resultsDir}/${fileName}.${fileExt}`;

  try {
    // Ensure output directory exists
    const dir = dirname(filePath);
    if (!existsSync(dir)) {
      mkdirSync(dir, { recursive: true });
    }

    // Create document in requested format
    let buffer: Buffer;
    if (format === 'pdf') {
      buffer = await createPdf(params);
    } else {
      const markdown = ''; // generateMarkdown(params) - no longer needed for PDF
      buffer = await createDocx(markdown, params);
    }

    // Write file to disk
    writeFileSync(filePath, buffer);

    console.log(`Document created: ${filePath}`);
    console.log(`Content length: ${buffer.length} bytes`);

    return {
      success: true,
      filePath,
      fileName: `${fileName}.${fileExt}`,
      fileSize: buffer.length,
      format,
      createdAt: new Date().toISOString()
    };

  } catch (error) {
    throw new Error(`Document creation failed: ${error instanceof Error ? error.message : 'Unknown error'}`);
  }
}

export default documentCreation;